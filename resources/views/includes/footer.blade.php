                        $('select[name = "color"]').append('<option value = "'+valueOfElement+'">'+valueOfElement+'</option>');
                        if (data.product_color == "") {
                            $('#product_color_area').hide();
                        } else {
                            $('#product_color_area').show();
                        }
                    });

